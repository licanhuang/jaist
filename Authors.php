<h3>2. Originality and Ethics:</h3>

Ensure that your manuscript presents original work that has not been published previously or is not under consideration for publication elsewhere.
Cite all relevant sources and obtain permission for any copyrighted material used in your paper.
Comply with ethical guidelines, including proper acknowledgment of funding sources, conflicts of interest, and obtaining informed consent for studies involving human subjects.

</br>
</br>

<h3>3. Copyright and Licensing:</h3>

Upon acceptance, authors will be required to sign a licensing agreement granting the journal the right to publish and distribute their work under the Creative Commons Attribution (CC BY) license.
This license allows others to share, adapt, and build upon your work as long as they provide proper attribution to the original source.

</br>
</br>
<h3>4. Submission:</h3>

Submit your manuscript through the online submission system available on the journal's website.
Ensure that your submission complies with all formatting and manuscript preparation guidelines provided on the submission platform.

</br>
</br>
<h3>5. Publication Charges:</h3>

JAIST follows an author-pays model, and there are article processing charges (APCs) associated with the publication.
Authors should be aware of the APCs and arrange for payment or seek information on any available waivers or discounts.

</br>
</br>
<h3>6. Response to Reviewers' Comments:</h3>

If your manuscript requires revisions based on reviewer feedback, address all comments and suggestions in a detailed response letter.
Clearly explain the changes made in the revised manuscript to help reviewers and editors assess the modifications.

</br>
</br>
<h3> 7. Open Access and Visibility: </h3>

JAIST is an open access journal, and your article will be freely available to readers worldwide, increasing its visibility and potential impact.
Engage with the journal's promotion efforts and share your work through your professional networks to maximize its reach.

