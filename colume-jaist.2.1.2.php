<p style ="font-size:8px;"><strong>Abstract</strong></p>
With the continuous improvement in various aspects in the field of artificial intelligence, the momentum of artificial intelligence with deep learning for the field of music is coming. The purpose of the research in this paper is to design a Bach style music authoring system based on deep learning. We use a LSTM neural network to train serialized and standardized music feature data. By repeated experiments, we find the optimal LSTM model which can generate imitation of Bach music. Finally the generated music is comprehensively evaluated with the form of online audition and Turing test. The repertoires which the music generation system constructed in this article are very close to the style of Bachâ€™s original music, and it is relatively difficult for ordinary people to distinguish the musics Bach authored from ones AI created.
