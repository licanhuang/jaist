<p style ="font-size:8px;"><strong>Abstract</strong></p>

In response to the evolving demands for innovative and entrepreneurial talents in the era of new engineering and artificial intelligence in Chinese society, after conducting a comprehensive analysis of the challenges currently faced by applied universities in the context of entrepreneurial education, it introduces a new model for nurturing innovative and entrepreneurial talents, coined as the "horizontal integration & vertical progression" model. It is driven by artificial intelligence technology and involves the revitalization of educational philosophies and innovative talent development strategies. It seamlessly integrates entrepreneurial education with specialized education, initiating a comprehensive reform in talent cultivation. In practical implementation, with the main force of reform, it is necessary to collaborate with strong related majors to carry out interdisciplinary and integrated training plans and curriculum system reforms, establish a "four dimensional integration" team of innovation and innovation mentors, and build a diversified innovation and training support platform. The implementation of the new model not only aids students better adapt to the needs of the new engineering and artificial intelligence era, but also promotes innovation in talent cultivation in applied universities, improves education quality, and enhances competitiveness.

